/**
 * Mouse cursor styles
 */
export enum MouseCursor {
    /** The default system cursor, typically an arrow. */
    Default = "default",

    /** The system cursor used when hovering over buttons and links, typically a hand. */
    Button = "pointer",

    /** An invisible cursor. */
    None = "none",

    /** Additional HTML cursor styles */
    CrossHair = "crosshair",
    Text = "text",
    Wait = "wait",
    Help = "help",
    Move = "move",
    Resize = "resize"
}
